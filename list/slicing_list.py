# process of dividing the list in some specfic range 

l=[1,2,3,2,3]

print(l[1:]) # index 1 incuded till last element 
print(l[:4]) #from 0 index will print the value till (4-1) index

print(l[::-1]) # will print the list in reversed order 
print(l[:2:]) # will print element with increase in last by 2
print(l)
